public class DoWhile {
	public static void main(String[] args) {
//do..while сначала выполняет тело цыкла, а потом проверяет условие, по этому тело цикла выполнится, как минимум, 1 раз
		 int i = 1;
        do {
            i++;
            System.out.print(i + " zzzzzz");
        } while(i < 5);
    }
}    